    getDefaultLocale: function () {
        var locale = "";
        try {
            var cookie_lang = window.localStorage.getItem("cookie_lang");
            if (cookie_lang) {
                locale = cookie_lang;
            } else {
                locale = navigator.language || navigator.userLanguage || '';
            }
        }catch (e) {

        }
        var t = /^en/.test(locale);
        return t ? "en" : "zh";
    }
